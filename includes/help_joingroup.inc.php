<h1 id="pt-contentheader">Help and Guidelines / Joining or Switching Groups</h1>
	<div>
		<h3>Joining a Practicum Group</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			On the left-hand side of the page, click the 'Find Group' item in the menu.
			<img src="images/joingroup_1.png" alt="joingroup_1"/>
		</li>
		<li>
			A new page will open. Type a group name or a word or phrase of a group name and click the 'Search' button.
			<img src="images/joingroup_2.png" alt="joingroup_2"/>
		</li>
		<li>
			Search results will show up below. Look for the group you want to join and click its 'Join Group' link.
			<img src="images/joingroup_3.png" alt="joingroup_4"/>
		</li>
		<li>
			When the switching is successful, you will be redirected to the requirements page.
		</li>
		</ol>
	</div>
	<br/>
	<div>
		<h3>Switching to another Practicum Group</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			You need to be a member of a group first before you can switch to another group. Follow steps 1 and 2 in 'Joining a Practicum Group'.
		</li>
		<li>
			Once you're able to find a practicum group, click its 'Switch to Group' link.
			<img src="images/joingroup_4.png" alt="joingroup|_4"/>
		</li>
		</ol>
	</div>
	</br>
	<div>
		<h3>Frequently Asked Questions</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			<i>Can I leave a Practicum Group?</i><br/>
			- No. Once you join a practicum group, you can only switch to another one, not leave. <br>  <br>
		</li>
		<li>
			<i>If I switch to another group, what will happen to my requirements?</i><br/>
			- They will all be removed to prevent anyone from spamming bogus requirements around different groups.  <br>  <br>
		</li>
		<li>
			<i>My practicum coordinator told me to join his group, but I can't find it. What's the problem?</i><br/>
			- The group might already be closed or deleted. You can only search for practicum groups that are open, and of course, not yet deleted.
		</li>
		</ol>
	</div>
