<h1 id="pt-contentheader">Help and Guidelines / Practicum Groups</h1>
	<p>
		A <b>Practicum Group</b> is a group of people managed by a Practicum Coordinator and is populated by Students and Linkage Officers.
	</p>
	<div>
		<h3>For Students</h3>
		<ul style="padding-left:15px; list-style-type:square; margin:0;">
			<li>Can <a href="/help.php?topic=joingroup">join or switch</a> practicum groups.</li>
			<li>Can only join one active or open group at a time.</li>
			<li>Can <a href="/help.php?topic=requirements">submit requirements</a> only when a member of a group.</li>
		</ul>
		<br/>
		<h3>For Linkage Officers</h3>
		<ul style="padding-left:15px; list-style-type:square; margin:0;">
			<li>Can <a href="/help.php?topic=joingroup">join or switch</a> practicum groups.</li>
			<li>Can only join one active or open group at a time.</li>
			<li>Can <a href="/help.php?topic=reqconfirmation">confirm or reject the requirements</a> of students who belong to the group he is a linkage officer to.</li>
		</ul>
		<br/>
		<h3>For Practicum Coordinators</h3>
		<ul style="padding-left:15px; list-style-type:square; margin:0;">
			<li>Can <a href="/help.php?topic=groupcontrols">create, delete, or close</a> practicum groups.</li>
			<li>Can only manage active or open group at a time.</li>
			<li>Can <a href="/help.php?topic=reqconfirmation">confirm or reject the requirements</a> of students who belong to the group he created.</li>
		</ul>
	</div>
</div>