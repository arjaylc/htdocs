<h1 id="pt-contentheader">Help and Guidelines / Related Data Searching</h1>
	<p>
		To prevent, redundant and ambigous records in the practicum tracking system, <b>Related Data Searching</b> is used. When filling up forms where data is most likely to be entered multiple times by multiple users, a search is conducted while the user is typing; the results can then be selected by the user.
	</p>

	<div>
		<h3>How It Works</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			As soon as the user begins typing (e.g. Hew), a search is conducted.
			<img src="images/relatedsearch_1.png" alt="relatedsearch_1"/>
		</li>
		<li>
			A list of related results is then displayed. The user can choose to select any of the results.
			<img src="images/relatedsearch_2.png" alt="relatedsearch_2"/>
		</li>
		<li>
			When the user clicks a result item (e.g. Hewlett Packard, Makati), its company information is automatically filled up.
			<img src="images/relatedsearch_3.png" alt="relatedsearch_3"/>
		</li>
		</ol>
	</div>
	<br/>
	<div>
		<h3>Frequently Asked Questions</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			<i>What if I add a new company?</i><br/>
			- If you choose to add a new company, you are free to do so. But please do not spam the system with unnecessary and bogus information as this can affect other users' experiences.
		</li>
		<li>
			<i>Can I add a company with the same name?</i><br/>
			- Yes, as long as the address is different from other companies with the same name.
		</li>
		<li>
			<i>Can I modify the information of existing company records?</i><br/>
			- No. We are strict with keeping the company records as consistent and integrated as possible. If the company you are looking for is not present, you may choose to add it as a new record.
		</li>
		<li>
			<i>Can I add new contact persons with the same name?</i><br/>
			- No. One person can't be two different people at the same time. However, you are free to edit the information of a contact person.
		</li>
		<li>
			<i>What if I modify the information of a contact person?</i><br/>
			- If you modify any information of a contact person, it will be reflected throughout the system. Meaning, anyone who searches for that person in the future will see the new information you entered.
		</li>
		<li>
			<i>How do you close the Search Results Box?</i><br/>
			- You can click any of the search results, which is the best and proper way. Or you can just press the 'ESC' or 'Escape' key.
		</li>
		<li>
			<i>Will abbreviations work?</i><br/>
			- Most of the time, it will. But we discourage users from entering abbreviations for company names as this can prove ineffective when searching for accurate data. For example: 'HP' can be interpreted as 'Hewlett Packard', but can also be interpreted as 'Harry Potter'.
		</li>
		</ol>
	</div>
