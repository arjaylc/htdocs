		<h3>Viewing Linkage Officers</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			On the left-hand side of the page, click 'Linkage Officers' in the menu.
			<img src="images/linkageofficer_1.png" alt="linkageofficer_1"/>
		</li>
		<li>
			By default, this displays all the linkage officers in your group alphabetically by their last name.
			<img src="images/linkageofficer_2.png" alt="linkageofficer_2"/>
		</li>
		<li>
			To view your linkage officers by company, click the 'View by Company' link near the top.
			<img src="images/linkageofficer_3.png" alt="linkageofficer_3"/>
		</li>
		</ol>
	</div>
	<br/>
	<div>
		<h3>Assigning Linkage Officers</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			On the left-hand side of the page, click 'Linkage Officers' in the menu.
			<img src="images/linkageofficer_1.png" alt="linkageofficer_1"/>
		</li>
		<li>
			In the next page, click the 'Assign Linkage Officers' link near the top.
			<img src="images/linkageofficer_4.png" alt="linkageofficer_4"/>
		</li>
		<li>
			This will display a list of companies, and under each company, a list of linkage officers. A checked linkage officer means he/she is already assigned to that company, and an unchecked one means otherwise.
			<img src="images/linkageofficer_5.png" alt="linkageofficer_5"/>
		</li>
		<li>
			To assign a linkage officer, just check the box beside his/her name under the company you want to assign him/her. To remove an assignment, simply uncheck his/her box.
			<img src="images/linkageofficer_6.png" alt="linkageofficer_6"/>
		</li>
		</ol>
	</div>
	<br/>
	<div>
		<h3>Frequently Asked Questions</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			<i>Can I assign multiple companies to one linkage officer?</i><br/>
			- Yes, but that will depend whether or not your linkage officer agrees with your decision. <br> <br>
		</li>
		<li>
			<i>What happens if I already assigned a linkage officer and he switched to another practicum group?</i><br/>
			- He will automatically be unassigned from the companies he was originally assigned to.
		</li>
		</ol>
