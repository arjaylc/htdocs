	<div>
		<h3>Frequently Asked Questions</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		<li>
			<i>Who can submit requirements?</i><br/>
			- Only students can submit requirements. <br> <br>
		</li>
		<li>
			<i>Can I modify a requirement that is still pending?</i><br/>
			- No. You need to wait for your Practicum Coordinator or Linkage Officer to reject your requirement before you can modify whatever was in there.  <br> <br>
		</li>
		<li>
			<i>Can I leave some fields blank?</i><br/>
			- In a few cases, yes. We discourage users from leaving fields blank as this can cause data inconsistency in the system. There are cases wherein we allow for empty values, like the 'Fax Number' text field. <br> <br>
		</li>
		<li>
			<i>Can I submit a requirement twice?</i><br/>
			- It depends on the requirement. Surveys and evaluation forms can only be submitted once. As for the requirements like the Project Details Form, you can submit another one if your previous submission was rejected.  <br> <br>
		</li>
		<li>
			<i>Can I modify details of a requirement that is already confirmed?</i><br/>
			- For now, we don't allow modifications if a requirement is already confirmed because of the fact that it has already been confirmed.   <br>
		</li>
		</ol>
