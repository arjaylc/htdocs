	<div>
		<h3>Frequently Asked Questions</h3>
		<ol style="padding-left:15px; margin:0 0 0 5px;">
		</li>
		<li>
			<i>Is there another way to create a group?</i><br/>
			- No. There are multiple occasions where we will notify you to create a group, but it's still in the same manner discussed above. <br> <br>
		</li>
		<li>
			<i>If I delete a group, what happens to the group members?</i><br/>
			- The group members in your group will have to search for another group to join. In addition to that, all their requirements will be deleted, and all practicum data that was collected will be gone too.  <br> <br>
		</li>
		<li>
			<i>What's the difference between deleting and closing a group?</i><br/>
			- When you delete a group, it's gone forever. When you close a group, you can't do anything with it anymore. It's good as deleted but the data is retained. We highly encourage practicum coordinators to close their group when their practicum program is over to avoid inconsistencies and inaccurate data in the system.  <br> <br>
		</li>
		<li>
			<i>Can I switch to another group when my current group is closed?</i><br/>
			- For now, yes, but only do so when you need to (e.g. You failed in your previous practicum program). This can cause inaccuracies in the summary report produced during the end of a practicum program.
		</li>
		</ol>
